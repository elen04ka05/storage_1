const tagsContainer = document.getElementById('tags-container')
const cardsContainer = document.getElementById('cards-container')

const createTagElement = name => {
  const tag = document.createElement('div')
  tag.className = 'rounded-full bg-slate-100 py-1 px-4 cursor-pointer hover:shadow-md transition'
  tag.textContent = name
  return tag
}

const createCardElement = (code, title, date) => {
  const card = document.createElement('div')
  card.className = 'border border-slate-200 w-60 h-80 cursor-pointer transition hover:shadow-lg shadow-slate-50 rounded-md'
  card.addEventListener('click', () => {
    window.location.href = '/snippet.html'
  })

  const p = document.createElement('p')
  p.className = 'p-4 border-b border-slate-200 h-56 whitespace-pre overflow-hidden text-ellipsis'
  p.textContent = code
  card.append(p)

  const footer = document.createElement('div')
  footer.className = 'p-4'
  card.append(footer)

  const header = document.createElement('h2')
  header.className = 'font-bold overflow-hidden text-ellipsis whitespace-nowrap'
  header.textContent = title
  footer.append(header)

  const time = document.createElement('time')
  time.className = 'text-slate-400'
  time.textContent = date
  footer.append(time)

  return card
}

exampleTags.forEach(name => {
  const tagElement = createTagElement(name)
  tagsContainer.append(tagElement)
})

exampleCards.forEach(card => {
  const cardElement = createCardElement(card.code, card.title, card.date)
  cardsContainer.append(cardElement)
})

